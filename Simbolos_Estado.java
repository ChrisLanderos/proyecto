public class Simbolos_Estado {
    private String m1;
    private String m2;
    public Simbolos_Estado(String m1, String m2) {
        this.m1 = m1;
        this.m2 = m2;          
    }
    
    public String getm1() {
        return m1;
    }

    public String getm2() {
        return m2;
    }
    public String toString() {
        return "Simbolos_Estado [m1=" + m1 + ", m2=" + m2 + "]";
    }
}
